package com.alee.extended.time;

/**
 * This enumeration represents available clock types.
 *
 * @author Mikle Garin
 */

public enum ClockType
{
    /**
     * Simple clock displaying time.
     */
    clock,

    /**
     * Stopwatch clock.
     */
    stopwatch,

    /**
     * Timer clock.
     */
    timer
}